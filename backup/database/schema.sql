-- Name: on_children_today_change(); Type: FUNCTION; Schema: public; Owner: -
--

CREATE FUNCTION public.on_children_today_change() RETURNS trigger
    LANGUAGE plpgsql
    AS $$
DECLARE
  cnt_today INT;
  cnt_now INT;
BEGIN
  -- Zähle Kinder dieser Gruppe
  SELECT 
    COUNT(*) FILTER (WHERE presence_today = 1),
    COUNT(*) FILTER (WHERE presence_now = 1)
  INTO cnt_today, cnt_now
  FROM children_today 
  WHERE group_id = NEW.group_id;

  -- Upsert in groups_today
  INSERT INTO groups_today (
    user_id, 
    group_id, 
    children_today, 
    children_now
  )
  VALUES (
    NEW.user_id, 
    NEW.group_id, 
    cnt_today, 
    cnt_now
  )
  ON CONFLICT (group_id) DO UPDATE
    SET 
      children_today = EXCLUDED.children_today,
      children_now = EXCLUDED.children_now,
      user_id = EXCLUDED.user_id;

  RETURN NEW;
END;
$$;


